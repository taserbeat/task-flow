-- マイグレーションで接続するユーザー
CREATE ROLE "tfAdmin" WITH
	LOGIN
	SUPERUSER
	CREATEDB
	CREATEROLE
	INHERIT
	REPLICATION
	BYPASSRLS
	CONNECTION LIMIT -1
	PASSWORD 'tfAdmin';

-- アプリケーションの運用で接続するユーザー
CREATE ROLE "tfApp" WITH
	LOGIN
	NOSUPERUSER
	NOCREATEDB
	NOCREATEROLE
	INHERIT
	REPLICATION
	NOBYPASSRLS
	CONNECTION LIMIT -1
	PASSWORD 'tfApp';
